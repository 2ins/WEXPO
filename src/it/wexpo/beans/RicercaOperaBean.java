package it.wexpo.beans;

public class RicercaOperaBean {
	
	private String operaNome;
	private String operaDescrizione;
	private String operaNomeEng;
	private String operaDescrizioneEng;
	private String tag;
	private String nazione;
	private String citta;
	private String utenteNome;
	private String utenteCognome;
	
	
	
	
	public String getOperaNome() {
		return operaNome;
	}
	public void setOperaNome(String operaNome) {
		this.operaNome = operaNome;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public String getNazione() {
		return nazione;
	}
	public void setNazione(String nazione) {
		this.nazione = nazione;
	}
	public String getCitta() {
		return citta;
	}
	public void setCitta(String citta) {
		this.citta = citta;
	}
	public String getUtenteNome() {
		return utenteNome;
	}
	public void setUtenteNome(String utenteNome) {
		this.utenteNome = utenteNome;
	}
	public String getUtenteCognome() {
		return utenteCognome;
	}
	public void setUtenteCognome(String utenteCognome) {
		this.utenteCognome = utenteCognome;
	}
	public String getOperaDescrizione() {
		return operaDescrizione;
	}
	public void setOperaDescrizione(String operaDescrizione) {
		this.operaDescrizione = operaDescrizione;
	}
	public String getOperaNomeEng() {
		return operaNomeEng;
	}
	public void setOperaNomeEng(String operaNomeEng) {
		this.operaNomeEng = operaNomeEng;
	}
	public String getOperaDescrizioneEng() {
		return operaDescrizioneEng;
	}
	public void setOperaDescrizioneEng(String operaDescrizioneEng) {
		this.operaDescrizioneEng = operaDescrizioneEng;
	}
	
	
	
}
